package com.swap;

public class SwapTwo {

	public static void main(String[] args) {
		
		int a,b,c;
	    a = 10;
	    b = 11;
	    c = 10;
	    System.out.println("Before Swapping The Number a="+a+ " b="+b);
	    
	    c = a;
	    a = b;
	    b = c;
	    System.out.println("After Swapping The Number a="+a+" b=" +b);

	}

}
